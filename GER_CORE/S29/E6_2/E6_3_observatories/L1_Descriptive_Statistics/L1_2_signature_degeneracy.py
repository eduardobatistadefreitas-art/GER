TITLE = "GER\nL1.2 Signature Degeneracy Observatory"


# ============================================================
# ANALYSIS
# ============================================================

def analyse(df: pd.DataFrame) -> dict:
    """
    Analyse signature degeneracy.
    """

    total_signatures = len(df)

    grouped = (
        df.groupby(list(df.columns))
        .size()
        .reset_index(name="Multiplicity")
        .sort_values("Multiplicity", ascending=False)
        .reset_index(drop=True)
    )

    unique_signatures = len(grouped)

    multiplicities = grouped["Multiplicity"].to_numpy()

    uniqueness_rate = (
        100.0 * unique_signatures / total_signatures
        if total_signatures
        else 0.0
    )

    degeneracy_rate = 100.0 - uniqueness_rate

    probabilities = multiplicities / multiplicities.sum()

    entropy = float(
        -np.sum(probabilities * np.log2(probabilities))
    )

    distribution = (
        grouped["Multiplicity"]
        .value_counts()
        .sort_index()
        .rename_axis("Multiplicity")
        .reset_index(name="Count")
    )

    top20 = grouped.head(20).copy()

    top20["Percentage"] = (
        100.0
        * top20["Multiplicity"]
        / total_signatures
    )

    summary = {

        "total_signatures": int(total_signatures),

        "unique_signatures": int(unique_signatures),

        "uniqueness_rate_percent": float(
            uniqueness_rate
        ),

        "degeneracy_rate_percent": float(
            degeneracy_rate
        ),

        "minimum_multiplicity": int(
            multiplicities.min()
        ),

        "maximum_multiplicity": int(
            multiplicities.max()
        ),

        "mean_multiplicity": float(
            multiplicities.mean()
        ),

        "median_multiplicity": float(
            np.median(multiplicities)
        ),

        "std_multiplicity": float(
            multiplicities.std()
        ),

        "entropy_bits": float(entropy),

        "status": "PASS",

    }

    return {

        "summary": summary,

        "unique_signatures": grouped,

        "distribution": distribution,

        "top20": top20,

  }
