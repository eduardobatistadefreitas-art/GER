# SAVE
# ============================================================

def save(storage: ExperimentStorage, result: dict) -> None:

    summary = result["summary"]

    storage.create_folder("report")
    storage.create_folder("tables")
    storage.create_folder("json")
    storage.create_folder("certificate")

    report_folder = storage.folder("report")
    tables_folder = storage.folder("tables")
    json_folder = storage.folder("json")
    certificate_folder = storage.folder("certificate")

    # --------------------------------------------------------
    # TABLES
    # --------------------------------------------------------

    result["unique_signatures"].to_csv(
        tables_folder / "unique_signatures.csv",
        index=False,
    )

    result["distribution"].to_csv(
        tables_folder / "multiplicity_distribution.csv",
        index=False,
    )

    result["top20"].to_csv(
        tables_folder / "top_signatures.csv",
        index=False,
    )

    # --------------------------------------------------------
    # JSON
    # --------------------------------------------------------

    with open(
        json_folder / "signature_degeneracy.json",
        "w",
        encoding="utf-8",
    ) as f:

        json.dump(
            summary,
            f,
            indent=4,
        )

    # --------------------------------------------------------
    # CERTIFICATE
    # --------------------------------------------------------

    with open(
        certificate_folder / "certificate.json",
        "w",
        encoding="utf-8",
    ) as f:

        json.dump(
            {
                "observatory": "L1.2",
                "title": "Signature Degeneracy Observatory",
                "status": summary["status"],
            },
            f,
            indent=4,
        )

    # --------------------------------------------------------
    # REPORT
    # --------------------------------------------------------

    report = f"""
============================================================
GER
L1.2 Signature Degeneracy Observatory
============================================================

Total Signatures
{summary['total_signatures']}

Unique Signatures
{summary['unique_signatures']}

Uniqueness (%)
{summary['uniqueness_rate_percent']:.6f}

Degeneracy (%)
{summary['degeneracy_rate_percent']:.6f}

Minimum Multiplicity
{summary['minimum_multiplicity']}

Maximum Multiplicity
{summary['maximum_multiplicity']}

Mean Multiplicity
{summary['mean_multiplicity']:.6f}

Median Multiplicity
{summary['median_multiplicity']:.6f}

Standard Deviation
{summary['std_multiplicity']:.6f}

Entropy (bits)
{summary['entropy_bits']:.6f}

Status
{summary['status']}

============================================================
"""

    with open(
        report_folder / "signature_degeneracy_report.txt",
        "w",
        encoding="utf-8",
    ) as f:

        f.write(report)

    print(report)


# ============================================================
# MAIN
# ============================================================

def run():

    storage = ExperimentStorage(

        experiment="S29_E6_2_L1_2",

        folders=[

            "report",

            "tables",

            "json",

            "certificate",

        ],

    )

    print("=" * 60)
    print(TITLE)
    print("=" * 60)

    df = load_signatures()

    result = analyse(df)

    save(storage, result)


# ============================================================
# ENTRY POINT
# ============================================================

if __name__ == "__main__":

    run()
