class CompactLogger:

    def __init__(self):

        self.start_time = time.time()

        self.last_update = self.start_time

    def banner(self):

        print()

        print("=" * 60)
        print("GER S29-E6.1")
        print("GLOBAL GEOMETRY SAMPLING")
        print("=" * 60)
        print()

    def log(self, text):

        now = datetime.now()

        line = (
            f"[{now:%H:%M:%S}] {text}"
        )

        print(line)

        with open(
            RUN_LOG,
            "a",
            encoding="utf-8"
        ) as f:

            f.write(line + "\n")

    def progress(

        self,

        current,

        total,

        accepted,

        rejected,

        rate

    ):

        percent = 100 * current / total

        elapsed = time.time() - self.start_time

        remaining = (
            total - current
        )

        eta = (
            remaining / rate
            if rate > 0
            else 0
        )

        hours = int(eta // 3600)

        minutes = int(
            (eta % 3600) // 60
        )

        bar_size = 30

        filled = int(
            percent / 100 * bar_size
        )

        bar = (
            "█" * filled +
            "░" * (bar_size - filled)
        )

        print()

        print("=" * 60)

        print(
            f"Progress : "
            f"{current:,}/{total:,}"
        )

        print(
            f"[{bar}] "
            f"{percent:6.2f}%"
        )

        print()

        print(
            f"Accepted : {accepted:,}"
        )

        print(
            f"Rejected : {rejected:,}"
        )

        print(
            f"Speed    : {rate:,.0f} geom/s"
        )

        print(
            f"ETA      : "
            f"{hours:02d}:{minutes:02d}"
        )

        print("=" * 60)

