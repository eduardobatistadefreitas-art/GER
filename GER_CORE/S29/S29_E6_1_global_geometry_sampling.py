class CheckpointManager:

    def __init__(self):

        self.data = {

            "processed": 0,

            "accepted": 0,

            "rejected": 0,

            "database_size": 0,

            "last_update": None,

            "random_seed": RANDOM_SEED

        }

    def exists(self):

        return CHECKPOINT_FILE.exists()

    def load(self):

        if not self.exists():

            return

        with open(
            CHECKPOINT_FILE,
            "r",
            encoding="utf-8"
        ) as f:

            self.data = json.load(f)

    def save(self):

        self.data[
            "last_update"
        ] = str(datetime.now())

        with open(
            CHECKPOINT_FILE,
            "w",
            encoding="utf-8"
        ) as f:

            json.dump(

                self.data,

                f,

                indent=4

            )

