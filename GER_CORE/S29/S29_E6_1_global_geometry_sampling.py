# Geometry Validator
# ============================================================

class GeometryValidator:

    """
    Rejects invalid geometries.

    The acceptance criteria may evolve
    during future GER experiments.
    """

    def __init__(self):

        pass

    # --------------------------------------------------------

    def validate(

        self,

        geometry,

        signature,

        observables

    ):

        if geometry["vertices"] < 2:

            return False

        if geometry["edges"] < geometry["vertices"] - 1:

            return False

        if geometry["density"] <= 0:

            return False

        if geometry["density"] > 1:

            return False

        if signature["Diameter"] <= 0:

            return False

        if np.isnan(

            observables["Entropy"]

        ):

            return False

        return True
