class StatisticsEngine:

    """
    Incremental statistical analysis of the sampled
    geometric space.
    """

    def __init__(self):

        self.observable_history = []

    # --------------------------------------------------------

    def update(self, observables):

        self.observable_history.append(
            observables
        )

    # --------------------------------------------------------

    def dataframe(self):

        if len(self.observable_history) == 0:

            return pd.DataFrame()

        return pd.DataFrame(
            self.observable_history
        )

    # --------------------------------------------------------

    def save_statistics(self):

        df = self.dataframe()

        if len(df) == 0:

            return

        summary = df.describe().T

        summary.to_csv(

            STATISTICS_DIR /
            "summary_statistics.csv"

        )

        correlation = df.corr(
            numeric_only=True
        )

        correlation.to_csv(

            STATISTICS_DIR /
            "correlation_matrix.csv"

        )

        covariance = df.cov(
            numeric_only=True
        )

        covariance.to_csv(

            STATISTICS_DIR /
            "covariance_matrix.csv"

        )

