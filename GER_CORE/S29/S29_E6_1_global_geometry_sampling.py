# Observable Extraction
# ============================================================

class ObservableExtractor:

    """
    Computes all observables stored
    in the scientific database.
    """

    def __init__(self):

        pass

    # --------------------------------------------------------

    def extract(

        self,

        geometry,

        signature

    ):

        n = geometry["vertices"]

        e = geometry["edges"]

        density = geometry["density"]

        degree = (

            2 * e / n

        )

        weighted_degree = (

            degree *

            density

        )

        average_distance = (

            signature["Diameter"] /

            2

        )

        maximum_distance = (

            signature["Diameter"]

        )

        clustering = (

            density ** 2

        )

        entropy_value = entropy(

            [

                density,

                1 - density

            ]

        )

        return {

            "Vertices":

                n,

            "Edges":

                e,

            "Density":

                density,

            "Degree":

                degree,

            "WeightedDegree":

                weighted_degree,

            "AverageDistance":

                average_distance,

            "MaximumDistance":

                maximum_distance,

            "Diameter":

                signature["Diameter"],

            "Convergence":

                signature["Convergence"],

            "Recurrence":

                signature["Recurrence"],

            "Harmonic":

                signature["Harmonic"],

            "Clustering":

                clustering,

            "Entropy":

                entropy_value

        }

