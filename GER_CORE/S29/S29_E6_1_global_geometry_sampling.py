# Signature Engine
# ============================================================

class SignatureEngine:

    """
    Converts a geometry into a GER signature.

    The implementation is intentionally modular.

    When the definitive GER engine is available,
    only this class needs to be replaced.
    """

    def __init__(self):

        pass

    # --------------------------------------------------------

    def compute(self, geometry):

        n = geometry["vertices"]

        e = geometry["edges"]

        density = geometry["density"]

        diameter = (

            math.log(n + 1)

            * (1.0 / density)

        )

        convergence = (

            density *

            np.sqrt(e)

        )

        recurrence = (

            abs(

                np.sin(

                    density * np.pi

                )

            )

        )

        harmonic = (

            1 /

            (1 + diameter)

        )

        return {

            "Diameter":

                diameter,

            "Convergence":

                convergence,

            "Recurrence":

                recurrence,

            "Harmonic":

                harmonic

        }

