class GlobalSampler:

    """
    Main controller of S29-E6.1.
    """

    def __init__(self):

        self.logger = CompactLogger()

        self.database = GeometryDatabase()

        self.generator = GeometryGenerator()

        self.signature_engine = SignatureEngine()

        self.extractor = ObservableExtractor()

        self.validator = GeometryValidator()

        self.statistics = StatisticsEngine()

        self.histograms = HistogramBuilder()

        self.density = DensityEstimator()

        self.checkpoint = CheckpointManager()

        if self.checkpoint.exists():

            self.checkpoint.load()

            self.logger.log(
                "Checkpoint restored."
            )

        self.processed = self.checkpoint.data[
            "processed"
        ]

        self.accepted = self.checkpoint.data[
            "accepted"
        ]

        self.rejected = self.checkpoint.data[
            "rejected"
        ]

        self.start_clock = time.time()

    # --------------------------------------------------------

    def speed(self):

        elapsed = max(

            time.time() -
            self.start_clock,

            1e-9

        )

        return self.processed / elapsed

    # --------------------------------------------------------

    def checkpoint_save(self):

        self.checkpoint.data[
            "processed"
        ] = self.processed

        self.checkpoint.data[
            "accepted"
        ] = self.accepted

        self.checkpoint.data[
            "rejected"
        ] = self.rejected

        self.checkpoint.data[
            "database_size"
        ] = self.accepted

        self.checkpoint.save()
          # --------------------------------------------------------

    def flush(self):

        self.database.flush()

        self.statistics.save_statistics()

        self.histograms.export()

        self.density.export()

    # --------------------------------------------------------

    def process_one_geometry(self):

        geometry = self.generator.generate()

        signature = self.signature_engine.compute(
            geometry
        )

        observables = self.extractor.extract(

            geometry,

            signature

        )

        valid = self.validator.validate(

            geometry,

            signature,

            observables

        )

        self.processed += 1

        if not valid:

            self.rejected += 1

            return

        self.accepted += 1

        self.database.append_geometry(

            geometry,

            signature,

            observables

        )

        self.statistics.update(
            observables
        )

        self.histograms.append(
            observables
        )

        self.density.add(
            observables
        )

    # --------------------------------------------------------

    def run(self):

        self.logger.banner()

        self.logger.log(
            f"Target geometries : {TOTAL_GEOMETRIES:,}"
        )

        self.logger.log(
            f"Checkpoint start  : {self.processed:,}"
        )

        while self.processed < TOTAL_GEOMETRIES:

            self.process_one_geometry()

            # --------------------------------------------

            if (
                self.accepted > 0
                and
                self.accepted % SAVE_INTERVAL == 0
            ):

                self.flush()

            # --------------------------------------------

            if (
                self.processed > 0
                and
                self.processed % CHECKPOINT_INTERVAL == 0
            ):

                self.checkpoint_save()

            # --------------------------------------------

            if (
                self.processed > 0
                and
                self.processed % STATISTICS_INTERVAL == 0
            ):

                self.statistics.save_statistics()

            # --------------------------------------------

            if (
                self.processed > 0
                and
                self.processed % LOG_INTERVAL == 0
            ):

                self.logger.progress(

                    self.processed,

                    TOTAL_GEOMETRIES,

                    self.accepted,

                    self.rejected,

                    self.speed()

                )

        # ------------------------------------------------

        self.flush()

        self.checkpoint_save()

        self.finish()

    # --------------------------------------------------------

    def finish(self):

        elapsed = time.time() - self.start_clock

        print()

        print("=" * 60)
        print("GLOBAL GEOMETRY SAMPLING FINISHED")
        print("=" * 60)

        print()

        print(f"Processed : {self.processed:,}")
        print(f"Accepted  : {self.accepted:,}")
        print(f"Rejected  : {self.rejected:,}")

        print()

        print(
            f"Elapsed   : {elapsed/3600:.2f} hours"
        )

        print(
            f"Average Speed : "
            f"{self.processed/max(elapsed,1):,.1f} geom/s"
        )

        print()

        print("Database")

        print(" ", GEOMETRIES_FILE)
        print(" ", SIGNATURES_FILE)
        print(" ", OBSERVABLES_FILE)

        print()

        print("Statistics")

        print(" ", STATISTICS_DIR)

        print()

        print("Histograms")

        print(" ", HISTOGRAM_DIR)

        print()

        print("Density Maps")

        print(" ", DENSITY_DIR)

        print()

        print("Logs")

        print(" ", LOG_DIR)

        print()

