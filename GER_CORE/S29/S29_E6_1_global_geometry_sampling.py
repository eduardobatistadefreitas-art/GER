class GeometryGenerator:

    """
    Generates admissible random geometries.

    This class is intentionally isolated.

    Future versions may replace the random generator
    by the official GER engine without modifying the
    rest of the experiment.
    """

    def __init__(self):

        pass

    # --------------------------------------------------------

    def random_vertices(self):

        return random.randint(

            MIN_VERTICES,

            MAX_VERTICES

        )

    # --------------------------------------------------------

    def random_edges(self, n):

        maximum = min(

            MAX_EDGES,

            n * (n - 1) // 2

        )

        minimum = max(

            MIN_EDGES,

            n - 1

        )

        return random.randint(

            minimum,

            maximum

        )

    # --------------------------------------------------------

    def generate(self):

        vertices = self.random_vertices()

        edges = self.random_edges(vertices)

        geometry = {

            "vertices": vertices,

            "edges": edges,

            "density":

                edges /

                (vertices * (vertices - 1) / 2),

            "seed":

                random.randint(

                    0,

                    2**32 - 1

                )

        }

        return geometry

