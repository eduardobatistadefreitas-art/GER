class GeometryDatabase:

    """
    Permanent storage.

    New executions append new geometries.

    Existing geometries are preserved.
    """

    def __init__(self):

        self.geometries = []

        self.signatures = []

        self.observables = []

    def append_geometry(

        self,

        geometry,

        signature,

        observables

    ):

        self.geometries.append(
            geometry
        )

        self.signatures.append(
            signature
        )

        self.observables.append(
            observables
        )
          # --------------------------------------------------------

    def flush(self):

        if len(self.geometries):

            df = pd.DataFrame(
                self.geometries
            )

            if GEOMETRIES_FILE.exists():

                previous = pd.read_parquet(
                    GEOMETRIES_FILE
                )

                df = pd.concat(
                    [
                        previous,
                        df
                    ],
                    ignore_index=True
                )

            df.to_parquet(
                GEOMETRIES_FILE,
                index=False
            )

        # ----------------------------------------------------

        if len(self.signatures):

            df = pd.DataFrame(
                self.signatures
            )

            if SIGNATURES_FILE.exists():

                previous = pd.read_parquet(
                    SIGNATURES_FILE
                )

                df = pd.concat(
                    [
                        previous,
                        df
                    ],
                    ignore_index=True
                )

            df.to_parquet(
                SIGNATURES_FILE,
                index=False
            )

        # ----------------------------------------------------

        if len(self.observables):

            df = pd.DataFrame(
                self.observables
            )

            if OBSERVABLES_FILE.exists():

                previous = pd.read_parquet(
                    OBSERVABLES_FILE
                )

                df = pd.concat(
                    [
                        previous,
                        df
                    ],
                    ignore_index=True
                )

            df.to_parquet(
                OBSERVABLES_FILE,
                index=False
            )

        self.clear()

    # --------------------------------------------------------

    def clear(self):

        self.geometries.clear()

        self.signatures.clear()

        self.observables.clear()

